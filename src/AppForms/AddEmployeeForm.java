    public void loadEmployeeID(){
        String sql = "SELECT COUNT(*) FROM employee";
        int empCount;
        try {
            ResultSet rs = DB.search(sql);
            if(rs.next()){
                empCount = rs.getInt(1);
                jTextField1.setText("EMP" + (empCount+1));
            }else{
                jTextField1.setText("ID NOT SPECIFIED");                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
